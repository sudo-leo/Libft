#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*dp;
	const char	*sp;

	if (!dst && !src)
		return (NULL);
	dp = dst;
	sp = src;
	while (n--)
		*dp++ = *sp++;
	return (dst);
}
